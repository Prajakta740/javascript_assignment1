/* Switch case for month  */
let month = prompt("\n Enter the number of the month you want search:");
  month = Number.parseInt(month);
  switch (month) {
    case 1:
      console.log("\n January.");
      break;
    case 2:
      console.log("\n February.");
      break;
    case 3:
      console.log("\n March.");
      break;
    case 4:
      console.log("\n April.");
      break;
    case 5:
      console.log("\n May.");
      break;
    case 6:
      console.log("\n June.");
      break;
    case 7:
      console.log("\n July.");
      break;
    case 8:
      console.log("\n August.");
      break;
    case 9:
      console.log("\n September.");
      break;
    case 10:
      console.log("\n October.");
      break;
    case 11:
      console.log("\n November.");
      break;
    case 12:
      console.log("\n December.");
      break;
    default:
      console.log("\n Invalid Input.");
      break;
}
  